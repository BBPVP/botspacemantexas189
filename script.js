// Fungsi untuk menghasilkan nilai acak dari array
function getRandomValue() {
  const randomOptions = ['1x', '2x', '3x', '4x', '5x', '6x', '7x', '8x', '9x', '11x', '12x', '13x', '14x', '15x', '16x', '17x', '18x', '19x', '20x'];
  return randomOptions[Math.floor(Math.random() * randomOptions.length)];
}

// Fungsi untuk memformat waktu
function formatTime(time) {
  const date = new Date(time * 1000); // Konversi waktu dari detik ke milidetik
  return date.toLocaleTimeString('en-US', { hour12: false, hour: '2-digit', minute: '2-digit' });
}

// Fungsi untuk mendapatkan nilai acak antara min dan max (inklusif)
function getRandomInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

// Fungsi untuk memformat waktu menjadi string (HH:MM)
function formatTime(timestamp) {
    const date = new Date(timestamp * 1000);
    return date.toLocaleTimeString('id-ID', { hour: '2-digit', minute: '2-digit', hour12: false });
}

// Fungsi untuk mendapatkan nilai acak untuk "Min Xx"
function getRandomValue() {
    return getRandomInt(3, 10); // Rentang acak untuk "Min Xx"
}

// Fungsi untuk menghitung interval dengan selisih acak antara 5-8 menit
function calculateIntervals() {
    const date = new Date();
    
    // Waktu mulai dihitung dari saat ini + interval acak pertama (5-8 menit)
    let targetTime = new Date(date.getTime() + getRandomInt(5, 8) * 60 * 1000);

    const intervals = [];
    intervals.push(`${formatTime(targetTime.getTime() / 1000)} - Min ${getRandomValue()}x ==> Cairkan`);

    // Generate 4 interval tambahan dengan selisih acak 5-8 menit
    for (let i = 1; i < 5; i++) {
        targetTime.setMinutes(targetTime.getMinutes() + getRandomInt(5, 8)); // Tambahkan waktu secara acak
        intervals.push(`${formatTime(targetTime.getTime() / 1000)} - Min ${getRandomValue()}x ==> Cairkan`);
    }

    return intervals;
}

